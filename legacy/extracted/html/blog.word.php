<h2>Les pièces détachées automobiles du marché de l'après-vente</h2>
<br>
Les pièces de l'après-vente sont utilisées pour remplacer directement les pièces officielles OEM/OES. En général, les accessoires de voiture du commerce secondaire ne sont pas sous licence constructeur automobile ce qui varie en termes de prix et de qualité, et offre une alternative intéressante pour se fournir en pièces compatibles avec votre véhicule.
<br>
Les pièces de rechange du marché secondaire sont nommées : pièces aftermarket ou génériques.
<br>
En ce qui concerne le contrôle qualité, de performance et de durée les pièces auto aftermarket n'ont pas de réglementation, mais apparaissent parfaitement conformes aux normes européennes
<br>
Pour des raisons évidentes de sécurité et de protection de l'environnement, les accessoires et pièces aftermarket doivent, être approuvés avant d'être mis sur le marché. Cette exigence de validité aux prescriptions techniques reste obligatoire.
<br>
L'agrément se traduit par l'obtention d'un certificat de conformité européen. Le fabricant est tenu de garantir que les composants et équipements sont produits dans les conditions auxquelles le brevet est délivré. Chaque pièce reçoit un marquage approprié.
<br>
L'homologation aide au renforcement de la sécurité routière et de la protection de l'environnement et les intérêts des consommateurs, des fabricants et des distributeurs pièces autos, en préservant la concurrence sur le marché des pièces détachées automobiles.
