<br>
Les organes mécaniques sont démontés, nettoyés, contrôlés et les éléments usés remplacés par des composants neufs. La partie mécanique est rétablie en état conformément aux spécifications du manufacturier, dans un atelier dont les moyens de réalisation et de vérification garantissent les caractéristiques testées sur un banc permettent au véhicule de conserver ses prestations d'origine.
<br>
La mention échange standard implique que le nom du constructeur ou du fabricant ayant remis entièrement à neuf la pièce soit inscrit de façon apparente sur tous les documents commerciaux.
<br>
Les pièces échange standard sont en général le moteur, le démarreur, l'alternateur, le turbo, la pompe d'injection, la boite de vitesse, certains éléments électroniques, etc.
<br>
Les avantages restent pour le client un excellent rapport qualité/prix l'économie réalisée par rapport à une pièce neuve peut dépasser les 50 % avec des performances aux mêmes spécificités que les produits d'origine.
