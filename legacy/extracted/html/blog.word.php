<h2>Le marché de la pièce automobile</h2>
<br>
Au début de l'air automobile, les premiers industriels de voitures se fournissent auprès des fabricants de pièces indépendants. Plus tard, les constructeurs automobiles ont racheté quelques prestataires afin de contrôler la fiabilité des composants fabriqués.
<br>
Les constructeurs de voitures prenaient donc en charge la ligne de conception structurée au sein du réseau d'élaboration et d'assemblage des véhicules. 
<br>
Parallèlement à cette ligne d'exploitation automobile, les industriels mettaient en place la réalisation de modules pour les nouveaux prototypes sous la nomination pièces OEM ( Original Equipment Manufacturer )
<br>
Les problèmes de logistique des pièces détachées dans l'activité automobile restent très complexes. Les entrepôts doivent offrir des perspectives de stockage, disponibles en quantité dans tout le pays. 
<br>
Notamment en raison des milliers de composants différents, destinées à de nombreuses variétés de modèles de voiture.
<br>
Par conséquent, garder un réseau complet de production n'était pas envisageable.
<br>
Pendant ce temps, d'autres prestataires de pièces détachées automobiles ont uni leurs forces pour créer des compagnies plus grandes et plus performantes.
<br>
Les manufacturiers automobiles ont donc ressollicité ces entrepreneurs transformés en de gros fournisseurs qui fabriquent des pièces pour diverses marques de voitures.
<br>
Ces fournisseurs de pièces détachées automobiles sont devenus ce que l'on appelle aujourd'hui des équipementiers première monte automobile.
<br>
Les composants produits dans le cadre du contrat entre les industriels et les nouvelles firmes doivent répondre à des normes de qualité spécifiques. En fait, le label constructeur contrôle entièrement le volet « contrôle qualité » du processus. Ces accessoires équipementiers sont connues sous le nom de pièces première monte d'origine OES ( Original Equipment Supplier ).
<br>
Pour garantir une arrivée continue et cohérente des pièces détachées automobiles, les constructeurs de voitures font appel à plusieurs équipementiers. 
<br>
Chaque fabricant suit la même procédure de conception pour maintenir une spécificité de réalisation et des caractéristiques fixées par un cahier des charges aux normes constructeur. Les industriels automobiles ont tendance à sous-traiter l'élaboration d'une pièce équivalente à plusieurs équipementiers pour assurer une productivité régulière.
<br>
Ainsi, si un manufacturier rencontre des problèmes avec une compagnie, cela n'empêchera pas le rendement des chaînes de production et permettra le remplacement par d'autres marques d'équipement.
<br>
Aujourd'hui existent des négociations entre diverses constructeurs qui préparent la même pièce OEM qui se retrouvera montée sur des véhicules de labels et modèles totalement différents. De multiples équipementiers fabriquent ces pièces OEM identique à la demande des manufacturiers.
<br>
Dansun premier temps cela réduire les coûts d'exploitation, et garder le monopole sur l'industrie automobile.
<br>
Nous assistons aussi à des accords entre équipementiers qui chacun fabriquent sur sa ligne de production le composant d'un ensemble de pièces OES qui seront assemblées plus tard en kit. Tout cela est mis en place pour réduire les coûts. Dans plusieurs cas vous trouverez un conditionnement avec une étiquette équipementier différent de la marque de la pièce emballer.
